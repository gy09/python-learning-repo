def addi(a, b):
    return a + b


def sub(a, b):
    return a - b


def mul(a, b):
    return a * b


def div(a, b):
    return a / b


def expon(a, b):
    return a ** b


a = float(input("Enter the value of num1"))
b = float(input("Enter the value of num2"))

print(addi(a, b))
print(sub(a, b))
print(mul(a, b))
print(div(a, b))
print(expon(a, b))
